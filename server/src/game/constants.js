function buildMotorRequiredLevels(count, maxLevel = 200) {
  const total = Math.max(0, Number(count) || 0)
  if (total <= 0) return []

  const levels = [1]
  if (total === 1) return levels
  levels.push(4)
  if (total === 2) return levels
  levels.push(7)
  if (total === 3) return levels
  levels.push(10)
  if (total === 4) return levels

  const remaining = total - levels.length
  const start = levels[levels.length - 1]
  const span = Math.max(1, maxLevel - start)
  const step = span / remaining

  for (let i = 1; i <= remaining; i += 1) {
    const target = Math.round(start + step * i)
    const prev = levels[levels.length - 1]
    levels.push(Math.max(prev + 1, target))
  }

  return levels
}

