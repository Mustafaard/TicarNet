function applyFleetProgression(catalog, options) {
  const list = Array.isArray(catalog) ? catalog : []
  if (list.length === 0) return

  const baseCash = Math.max(1, Number(options?.baseCash || 1))
  const baseEngineKits = Math.max(0, Number(options?.baseEngineKits || 0))
  const baseSpareParts = Math.max(0, Number(options?.baseSpareParts || 0))
  const baseIncome = Math.max(1, Number(options?.baseIncome || 1))
  const baseFuel = Math.max(0, Number(options?.baseFuel || 0))
  const baseEnergy = Math.max(0, Number(options?.baseEnergy ?? baseFuel))
  const baseXp = Math.max(1, Number(options?.baseXp || 10))
  const baseCement = Math.max(0, Number(options?.baseCement || 0))
  const baseTimber = Math.max(0, Number(options?.baseTimber || 0))
  const baseBrick = Math.max(0, Number(options?.baseBrick || 0))

  list.forEach((entry, index) => {
    const safeIndex = Math.max(0, Number(index) || 0)
    const levelMultiplier = Math.max(
      1,
      Math.trunc(Number(entry?.levelRequired || safeIndex + 1) || (safeIndex + 1)),
    )

    entry.cash = Math.max(1, Math.round(baseCash * levelMultiplier))
    entry.engineKits = Math.max(0, Math.round(baseEngineKits * levelMultiplier))
    entry.spareParts = Math.max(0, Math.round(baseSpareParts * levelMultiplier))
    entry.rentPerHour = Math.max(1, Math.round(baseIncome * levelMultiplier))
    entry.fuel = Math.max(0, Math.round(baseFuel * levelMultiplier))
    entry.energy = Math.max(0, Math.round(baseEnergy * levelMultiplier))
    if (baseCement > 0 || Math.max(0, Number(entry?.cement || 0)) > 0) {
      entry.cement = Math.max(0, Math.round(baseCement * levelMultiplier))
    }
    if (baseTimber > 0 || Math.max(0, Number(entry?.timber || 0)) > 0) {
      entry.timber = Math.max(0, Math.round(baseTimber * levelMultiplier))
    }
    if (baseBrick > 0 || Math.max(0, Number(entry?.brick || 0)) > 0) {
      entry.brick = Math.max(0, Math.round(baseBrick * levelMultiplier))
    }
    entry.xp = Math.max(1, Math.round(baseXp * levelMultiplier))
  })
}

